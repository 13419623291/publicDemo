package com.liucheng;

public class Paixu {

	public static void main(String[] args) {
		//制定n1位100(整型)
		int n1=100;
		//当n1小于200是开始循环，大于200则跳出
		while (n1<200){
			n1=n1+2;
		System.out.println(n1);
		}
		//定义n2为100(整型)  当n2小于200是开始循环，大于200则跳出
		for (int n2=100;n2<200;){
			n2=n2+2;
			System.out.println(n2);
		}
		

	}

}
